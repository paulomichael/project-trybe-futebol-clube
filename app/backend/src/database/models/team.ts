import { Model, STRING } from 'sequelize';
import db from '.';
// import OtherModel from './OtherModel';
import Match from './match';

class Team extends Model {
  // public <campo>!: <tipo>;
  teamName: string;
}

Team.init({
  // ... Campos
  teamName: STRING,
}, {
  // ... Outras configs
  underscored: true,
  sequelize: db,
  modelName: 'team',
  timestamps: false,
});

